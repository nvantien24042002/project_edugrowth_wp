      <h4 class="heading-title text-left">Ecosystem Partners</h4>
      <div class="box-ecosystem">
          <p>
              Ecosystem partnerships are with local and international organisations
              that align or compliment our programs, events and initiatives.
          </p>
          <p>
              Ecosystem partnership arrangements offer various benefits to EduGrowth
              and our members. Arrangements can include co-hosting events, promotion
              of shared knowledge, resources and services, co-authoring research,
              offering reciprocal access to programs and services, and increasing our
              network and reach.
